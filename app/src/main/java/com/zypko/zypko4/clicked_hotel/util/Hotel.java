package com.zypko.zypko4.clicked_hotel.util;

public class Hotel {

    private int hotel_id;
    private String hotel_name;
    private String food_category_description;
    private String food_category_name;
    private String hotel_address;
    private String hotel_image;
    private String hotel_category;
    private String hotel_rating;
    private String hotel_station;


    public String getHotel_rating() {
        return hotel_rating;
    }

    public void setHotel_rating(String hotel_rating) {
        this.hotel_rating = hotel_rating;
    }

    public int getHotel_id() {
        return hotel_id;
    }

    public void setHotel_id(int hotel_id) {
        this.hotel_id = hotel_id;
    }

    public String getHotel_name() {
        return hotel_name;
    }

    public void setHotel_name(String hotel_name) {
        this.hotel_name = hotel_name;
    }

    public String getFood_category_description() {
        return food_category_description;
    }

    public void setFood_category_description(String food_category_description) {
        this.food_category_description = food_category_description;
    }

    public String getFood_category_name() {
        return food_category_name;
    }

    public void setFood_category_name(String food_category_name) {
        this.food_category_name = food_category_name;
    }

    public String getHotel_address() {
        return hotel_address;
    }

    public void setHotel_address(String hotel_address) {
        this.hotel_address = hotel_address;
    }

    public String getHotel_image() {
        return hotel_image;
    }

    public void setHotel_image(String hotel_image) {
        this.hotel_image = hotel_image;
    }

    public String getHotel_category() {
        return hotel_category;
    }

    public void setHotel_category(String hotel_category) {
        this.hotel_category = hotel_category;
    }


    public String getHotel_station() {
        return hotel_station;
    }

    public void setHotel_station(String hotel_station) {
        this.hotel_station = hotel_station;
    }
}
